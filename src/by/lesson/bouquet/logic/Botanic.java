package by.lesson.bouquet.logic;

public class Botanic {
	private int emptyLeaves = 0;
	private int fullLeaves = 0;

	public int getEmptyLeaves() {
		return emptyLeaves;
	}

	public void setEmptyLeaves(int emptyLeaves) {
		this.emptyLeaves = emptyLeaves;
	}

	public int getFullLeaves() {
		return fullLeaves;
	}

	public void setFullLeaves(int fullLeaves) {
		this.fullLeaves = fullLeaves;
	}
}
